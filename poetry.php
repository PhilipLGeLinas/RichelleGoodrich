                    and I heard a man inquire if I was weak.<br/><br/>
                    “You look pale, my dearest.  Are you ill?  Your skin’s in need of sun.”<br/>
                    I felt big fingers cup my face as I awoke.<br/>
                    And for a moment it was if I had an onset of amnesia<br/>
                    ‘til I recognized my husband, and I spoke.<br/><br/>
                    “It is you!” I cried. “My darling, you’ve returned to me at last!”<br/>
                    He hugged me tight, and in his ear I breathed a sigh.<br/>
                    “How I’ve missed you!”  “Oh, I’ve missed you too, but sadly I can’t stay.”<br/>
                    A cold remark to which I gravely uttered, “Why?”<br/><br/>
                    “There’s important work to do, my love.  Please try to understand.<br/>
                    It is our future for which business doth provide.<br/>
                    But I promise I shall not be long.  One week and I’ll return.”<br/>
                    He smiled softly while my tears I blinked aside.<br/><br/>
                    He then showered me in gifts, so I put on a glad expression<br/>
                    and accepted dainty trinkets and a ring.<br/>
                    I was grateful for the night we shared exchanging warm affections,<br/>
                    but by morning he was flittering his wings.<br/><br/>
                    “Must you fly from me so soon?” I asked, already feeling lonesome.<br/>
                    “You could sit a spell and share a pot of tea.”<br/>
                    With a hand upon my cheek he pacified me with a kiss.<br/>
                    “I’m sorry, dearest, but I’ll be home soon—you’ll see.”<br/><br/>
                    Now, before I said goodbye I made him swear to backtrack quickly.<br/>
                    He assured me it was just a few more days.<br/>
                    “I’ll be standing on our doorstep by this very hour next weekend.<br/>
                    Hear my promise; I shall rush and not delay.”<br/><br/>
                    Late that evening I revisited the moonlit grassy meadow.<br/>
                    There I found the ghostly maiden shedding tears.<br/>
                    Strands of haze were misted sorrow that fell o’er her empty body;<br/>
                    She was mourning loss of life, so it appeared.<br/><br/>
                    I rushed over, arms outstretched as if to offer an embrace,<br/>
                    but when I reached the girl my hands dropped to my thighs.<br/>
                    A dismal exhale crossed my lips; my features twisted with compassion.<br/>
                    No one spoke until the mourner raised her eyes.<br/><br/>
                    I was shocked when she proceeded to recount her day of death<br/>
                    by first confessing that a man had won her heart.<br/>
                    They had proved their love in secret when society forbade them,<br/>
                    though in open view they spent their time apart.<br/><br/>
                    Months elapsed and turned to years while their love blossomed undiscovered,<br/>
                    yet they yearned for more than meetings in the dark.<br/>
                    But alas!  The unforgiving world denied them any refuge.<br/>
                    To the afterlife they both vowed to embark.<br/><br/>
                    It was here inside this same secluded circle they met up<br/>
                    to swear their love to one another evermore.<br/>
                    If the world refused a nuptial kiss for man and wife to wed,<br/>
                    the pitying angels would hold open heaven’s door.<br/><br/>
                    Beneath a harvest moon they spent their last devoted hours,<br/>
                    resolute to make the final sacrifice.<br/>
                    Star-crossed lovers held up vials as they toasted their affections.<br/>
                    To their lips they put the poison and imbibed.<br/><br/>
                    But that wasn’t true.  Her sweetheart hesitated as she swallowed.<br/>
                    Not a drop of poison touched the craven’s tongue.<br/>
                    First confusion, then betrayal, lastly fear sunk in to haunt her<br/>
                    knowing there was no reversing what she’d done.<br/><br/>
                    She collapsed and breathed her final dying breath among the daisies<br/>
                    while her living lover muttered deep regrets.<br/>
                    He scurried off, a single kiss upon her icy hand in parting--<br/>
                    wanton cowardice she never would forget.<br/><br/>
                    She remained night after night beside her still and frigid body,<br/>
                    where the moon’s full eye had witnessed bitter woe.<br/>
                    And there she meant to haunt the woods until his passing made things right,<br/>
                    for she had nobody and nowhere else to go.<br/><br/>
                    A well of tears I shed at hearing her disastrous tale of heartbreak,<br/>
                    and upon its end she questioned where I’d been.<br/>
                    Disappointing her the prior night had caused a valid worry<br/>
                    that, just like her love, I’d ne’er return again.<br/><br/>
                    I apologized and then began the tale of my own sorrows,<br/>
                    how essentially I lived each day forlorn.<br/>
                    Though I loved my husband dearly and I longed to have him near,<br/>
                    his frequent travels meant he scarcely stayed at home.<br/><br/>
                    We connected much like sisters and divulged a wealth of secrets.<br/>
                    In our misery, we howled up at the moon.<br/>
                    For the first time in my life I felt both understood and pitied.<br/>
                    It was hard to part when morning came so soon.<br/><br/>
                    Daylight hours I slept away until the moon became my sunshine.<br/>
                    After dusk, I basked in treasured company,<br/>
                    until one windy autumn night a whispered wish disturbed my thoughts;<br/>
                    my ghostly sister bid eternity with me.<br/><br/>
                    She said there was yet another vial of poison, left untouched.<br/>
                    Her fleeing lover had abandoned it in haste.<br/>
                    She suggested that if someone sought to reach the world beyond<br/>
                    the vial’s contents would require but a taste.<br/><br/>
                    I’ll admit at first the notion was distressing to my mind.<br/>
                    “I have a husband and a home and seeds to sow!”<br/>
                    My spirit sister forced a smile.  “And so you shall….at least a while.<br/>
                    Though eventually all treasures you’ll forgo.”<br/><br/>
                    I understood her subtle meaning: now or later ends the same.<br/>
                    But giving up my now seemed wasteful and unwise.<br/>
                    “You forget what you’d be gaining—an eternity together.<br/>
                    What you’d lose are lonely days that you despise.”<br/><br/>
                    At the leading rays of sunrise, I proceeded toward my home.<br/>
                    It was impossible to sleep a wink that day.<br/>
                    Call it madness.  Call it reason from an otherworld perspective.<br/>
                    The allure to join my friend had taken sway.<br/><br/>
                    She was there for me.  A ghost!  Not now and then but every evening.<br/>
                    While the flesh-and-blood I’d married, he was gone.<br/>
                    Though he’d promised one week prior to return at dawning light,<br/>
                    my sole companion was an owl the whole day long.<br/><br/>
                    Pulled apart by clashing wants, I chose to stay the night at home<br/>
                    and pray my husband would arrive before the dew.<br/>
                    I yearned to speak to him of love and verify his heart’s desire,<br/>
                    but the only voice I heard kept crying, “Who!”<br/><br/>
                    So I contemplated hour by hour that one repeated word,<br/>
                    and in the morning I continued wide awake.<br/>
                    As the owl and I “whooed” out for you, my tears turned to a river.<br/>
                    And the sun, he traveled slowly for my sake.<br/><br/>
                    And I waited.<br/>
                    Oh, I waited!  ‘til the sky turned red with envy!<br/>
                    But you didn’t come to beg me stay with you.<br/>
                    Hence, my darling, where one lay now there are two.<br/><br/>
                </p>
