                <h2 class="main titles" style="font-family: Herculanum;">American Author, Novelist, & Poet</h2><br/>
                <p class="main"><q><i>
                            I like bubbles in everything. I respect the power of silence. In cold or warm weather I
                            favor a mug
                            of hot cocoa.
                            I admire cats–their autonomy, grace and mystery. I awe at the fiery colors in a sunset. I
                            believe in
                            deity.
                            I hear most often with my eyes, and I will trust a facial expression before any accompanying
                            comment. I invent
                            rules, words, adventures, and imaginary friends. I pretend something wonderful every day. I
                            will
                            never quit pretending.
                    </q><br/><br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                    - Richelle E. Goodrich</i>
                </p><br/><br/>
                <p style="text-align: left;">
                    I was born in Odgen, Utah in 1968 and spent the next couple of years growing up in my mother’s care
                    (helped by her family) while my father served as an Army Sergeant in the Vietnam War. Luckily, he
                    returned home safely, and I made the adjustment to having a permanent dad in the house. My brother
                    was born shortly afterwards, and all four of us moved to Washington State where my parents
                    discovered that not every inch of the so-called Evergreen State is actually green. In truth, a large
                    portion mimics the desert—which is where we set down roots.<br/><br/>
                    My parents completed their family with two additional daughters that resulted in three girls to one
                    boy, a circumstance that made sibling rivalry unequal and often unfair. My brother ended out with
                    his own room while I shared a room with my two sisters—an already-small area that was divided with
                    masking tape into personal corners. You can imagine the childish fuss that was made when someone had
                    to step across a dividing line to enter the only closet or to make a run for the bathroom!<br/><br/>
                    I was very blessed to have a mother who was willing to enroll me in ballet, tap dance, gymnastics,
                    girl scouts, piano lessons, as well as a short bout with the viola and the guitar. She faithfully
                    drove me from school to lessons to home for years. Any artistic talent I have is owed to my parents.
                    My dad sketched cartoon characters for me and my siblings when we were children, while my mom
                    created beautiful oil paintings. Their examples inspired me to pursue art in both high school and
                    college.<br/><br/>
                    After growing up in a small, desert town, I graduated from the local high school with honors. From
                    there, I worked my way through college at Eastern Washington University where I received two
                    bachelor’s degrees: one, a BA in Liberal Studies and the other, a BAEd in Math/Natural Sciences
                    Education. It wasn’t until I was older, married with three boys, that I developed an interest in
                    writing. That in itself is a long, curious story which you can read about on my author blog. Since
                    then, I have self-published ten books. My book quotes have been published in a number of places
                    including the Oxford Philosophy Being Human Course Book, Chicken Soup for the Soul: Christmas in
                    Canada, and in a Revlon magazine ad campaign.<br/><br/>
                    Writing has become a sweet adventure. I write whenever and wherever opportunity presents itself,
                    even during waits in line at the grocery store. I have learned to use bits of time wisely, knowing
                    that small accomplishments add up quickly. I prefer to write in complete silence whenever possible;
                    it seems my muses are more apt to visit me in a calm and quiet atmosphere. If you want a glimpse
                    into my head, I am an Alice in Wonderland girl—curious, creative, observant, always pondering,
                    always learning.
                </p>
